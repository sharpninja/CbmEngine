/// <summary>
/// A bundled C64-style 8x8 bitmap font (uppercase A-Z, digits 0-9, space). Each glyph is 8 bytes,
/// one per row, MSB = leftmost pixel. Missing glyphs return an all-zero (blank) glyph.
/// </summary>
public static class CbmFont8x8
{
    private static readonly byte[] Blank = new byte[8];

    private static readonly byte[][] Letters =
    {
        new byte[] { 0x18, 0x3C, 0x66, 0x66, 0x7E, 0x66, 0x66, 0x00 }, // A
        new byte[] { 0x7C, 0x66, 0x66, 0x7C, 0x66, 0x66, 0x7C, 0x00 }, // B
